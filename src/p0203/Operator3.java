package p0203;

public class Operator3 {

	public static void main(String[] args) {
		//논리연산자
		boolean result = true && false;
		System.out.println(result);
		
		result = true || false;
		System.out.println(result);
		
		result = !true;  //부정연산자-결과값을 반대로 도출
		System.out.println(result);
	}
}
